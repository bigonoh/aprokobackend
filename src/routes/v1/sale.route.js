const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const saleValidation = require('../../validations/sale.validation');
const salesController = require('../../controllers/sales.controller');

const router = express.Router();

router
  .route('/')
  .post(auth(), validate(saleValidation.createSale), salesController.createSale)
  .get(auth(), validate(saleValidation.getSales), salesController.getSales);

router
  .route('/:saleId')
  .get(auth(), validate(saleValidation.getSale), salesController.getSale)
  .patch(auth('manageSales'), validate(saleValidation.updateSale), salesController.updateSale)
  .delete(auth('manageSales'), validate(saleValidation.deleteSale), salesController.deleteSale);

module.exports = router;

